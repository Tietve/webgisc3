export const moduleOrder = Object.keys(moduleCatalog);

export const getModuleCatalog = (moduleCode) => moduleCatalog[moduleCode] || null;

export const matchLayerGuide = (moduleCode, layerName = '') => {
  const module = getModuleCatalog(moduleCode);
  if (!module) return null;
  const lowerName = layerName.toLowerCase();
  return module.essentialLayers.find((item) => lowerName.includes(item.keyword.toLowerCase())) || null;
};
